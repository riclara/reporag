function detectRepoRoot(startDir: string): string {
  const initial = path.resolve(startDir);
  let current = initial;
  let nearestReporagRoot: string | null = null;
  const packageJsonRoots: string[] = [];

  while (true) {
    if (fs.existsSync(path.join(current, ".git"))) {
      return current;
    }

    if (fs.existsSync(path.join(current, ".reporag"))) {
      nearestReporagRoot = current;
    }

    if (fs.existsSync(path.join(current, "package.json"))) {
      packageJsonRoots.push(current);
    }

    const parent = path.dirname(current);
    if (parent === current) {
      break;
    }

    current = parent;
  }

  const highestPackageJsonRoot =
    packageJsonRoots.length > 0
      ? packageJsonRoots[packageJsonRoots.length - 1]
      : null;

  return nearestReporagRoot ?? highestPackageJsonRoot ?? initial;
}

function readConfig(configPath: string): ReporagConfig | null {
  if (!fs.existsSync(configPath)) {
    return null;
  }

  const raw = JSON.parse(fs.readFileSync(configPath, "utf8")) as unknown;
  return parseReporagConfig(raw);
}

function writeConfig(configPath: string, config: ReporagConfig): void {
  fs.writeFileSync(configPath, JSON.stringify(config, null, 2), "utf8");
}

