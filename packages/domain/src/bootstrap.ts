function ensureGitIgnore(repoRoot: string): void {
  const gitIgnorePath = path.join(repoRoot, ".gitignore");

  const current = fs.existsSync(gitIgnorePath)
    ? fs.readFileSync(gitIgnorePath, "utf8")
    : "";

  const requiredEntries = [
    ".reporag/",
    ".codex/config.toml",
    ".codex/skills/reporag-mcp-usage/",
    ".mcp.json",
    ".claude/settings.local.json",
    ".claude/settings.json",
    ".gemini/settings.json",
  ];
  const normalizedLines = new Set(
    current
      .split(/\r?\n/u)
      .map((line) => line.trim())
      .filter((line) => line.length > 0),
  );
  const missingEntries = requiredEntries.filter(
    (entry) => !normalizedLines.has(entry) && !normalizedLines.has(`/${entry}`),
  );

  if (missingEntries.length === 0) {
    return;
  }

  const prefix =
    current.length > 0 && !current.endsWith("\n") ? `${current}\n` : current;
  const next = `${prefix}${missingEntries.join("\n")}\n`;

  fs.writeFileSync(gitIgnorePath, next, "utf8");
}

function writeJsonFile(filePath: string, value: Record<string, unknown>): void {
  fs.mkdirSync(path.dirname(filePath), { recursive: true });
  fs.writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function resolveMcpServerEntryPath(
  repoRoot: string,
  cliEntryPath?: string,
): string | null {
  const candidates = [
    cliEntryPath,
    path.join(repoRoot, "packages", "cli", "bundle", "reporag.cjs"),
    path.join(repoRoot, "packages", "cli", "dist", "cli.js"),
    path.join(process.cwd(), "packages", "cli", "bundle", "reporag.cjs"),
    path.join(process.cwd(), "packages", "cli", "dist", "cli.js"),
  ].filter((candidate): candidate is string => Boolean(candidate));

  for (const candidate of candidates) {
    const absoluteCandidate = path.resolve(candidate);
    if (fs.existsSync(absoluteCandidate)) {
      return fs.realpathSync(absoluteCandidate);
    }
  }

  return null;
}

function buildMcpWrapperSource(repoRoot: string, cliRuntimePath: string): string {
  return [
    "const { spawn } = require('node:child_process');",
    "",
    `const repoRoot = ${JSON.stringify(repoRoot)};`,
    `const cliRuntimePath = ${JSON.stringify(cliRuntimePath)};`,
    "",
    "const child = spawn(",
    "  process.execPath,",
    "  [cliRuntimePath, 'mcp', 'serve'],",
    "  {",
    "    cwd: repoRoot,",
    "    env: { ...process.env, REPORAG_REPO_ROOT: repoRoot },",
    "    stdio: 'inherit',",
    "  },",
    ");",
    "",
    "const forwardSignal = (signal) => {",
    "  if (!child.killed) {",
    "    child.kill(signal);",
    "  }",
    "};",
    "",
    "process.once('SIGINT', () => {",
    "  forwardSignal('SIGINT');",
    "});",
    "",
    "process.once('SIGTERM', () => {",
    "  forwardSignal('SIGTERM');",
    "});",
    "",
    "child.on('exit', (code, signal) => {",
    "  if (signal) {",
    "    process.kill(process.pid, signal);",
    "    return;",
    "  }",
    "",
    "  process.exit(code ?? 1);",
    "});",
    "",
    "child.on('error', (error) => {",
    "  console.error('Failed to launch reporag MCP server:', error);",
    "  process.exit(1);",
    "});",
    "",
  ].join("\n");
}

function ensureMcpWrapper(repoRoot: string, cliEntryPath?: string): string {
  const wrapperPath = path.join(repoRoot, PROJECT_MCP_WRAPPER_RELATIVE_PATH);
  const cliRuntimePath = resolveMcpServerEntryPath(repoRoot, cliEntryPath);

  if (!cliRuntimePath) {
    throw new Error("Unable to locate reporag CLI runtime for MCP wrapper generation");
  }

  fs.mkdirSync(path.dirname(wrapperPath), { recursive: true });
  fs.writeFileSync(
    wrapperPath,
    buildMcpWrapperSource(repoRoot, cliRuntimePath),
    "utf8",
  );

  return wrapperPath;
}

function resolveMcpCommandConfig(
  repoRoot: string,
  cliEntryPath?: string,
): McpCommandConfig {
  const wrapperPath = ensureMcpWrapper(repoRoot, cliEntryPath);

  return {
    command: process.execPath,
    args: [wrapperPath],
    cwd: repoRoot,
  };
}

function ensureMcpClientConfigs(repoRoot: string, cliEntryPath?: string): void {
  const codexPath = path.join(repoRoot, ".codex", "config.toml");
  const claudeMcpPath = path.join(repoRoot, ".mcp.json");
  const claudeSettingsPath = path.join(repoRoot, ".claude", "settings.local.json");
  const claudeSharedSettingsPath = path.join(
    repoRoot,
    PROJECT_CLAUDE_SHARED_SETTINGS_RELATIVE_PATH,
  );
  const geminiSettingsPath = path.join(repoRoot, ".gemini", "settings.json");
  const mcpCommand = resolveMcpCommandConfig(repoRoot, cliEntryPath);

  fs.mkdirSync(path.dirname(codexPath), { recursive: true });
  fs.writeFileSync(codexPath, buildCodexMcpConfig(mcpCommand), "utf8");
  writeJsonFile(claudeMcpPath, buildClaudeMcpConfig(mcpCommand));
  writeJsonFile(claudeSettingsPath, buildClaudeSettingsConfig());
  writeJsonFile(claudeSharedSettingsPath, buildClaudeSettingsConfig());

  const existingGeminiSettings = fs.existsSync(geminiSettingsPath)
    ? (JSON.parse(fs.readFileSync(geminiSettingsPath, "utf8")) as Record<string, unknown>)
    : null;
  writeJsonFile(
    geminiSettingsPath,
    buildGeminiSettingsConfig(existingGeminiSettings, mcpCommand),
  );
}

export function initRepository(startDir: string, cliEntryPath?: string): InitResult {
  try {
    const repoRoot = detectRepoRoot(startDir);

    const { repoDir, dbPath } = ensureReporagLayout(repoRoot);
    const configPath = path.join(repoDir, "config.json");

    const alreadyInitialized = fs.existsSync(configPath);
    const nextConfig = alreadyInitialized
      ? readConfig(configPath) ?? parseReporagConfig(DEFAULT_REPORAG_CONFIG)
      : parseReporagConfig(DEFAULT_REPORAG_CONFIG);

    writeConfig(configPath, nextConfig);

    ensureDatabase(dbPath);
    ensureMcpClientConfigs(repoRoot, cliEntryPath);
    ensureAgentGuidance(repoRoot);
    ensureClaudeGuidance(repoRoot);
    ensureGitIgnore(repoRoot);

    return {
      ok: true,
      repoRoot,
      configPath,
      dbPath,
      alreadyInitialized,
    };
  } catch (error) {
    const repoRoot = detectRepoRoot(startDir);

    return {
      ok: false,
      repoRoot,
      errorCode: "FS_ERROR",
      message:
        error instanceof Error ? error.message : "Unknown error during init",
    };
  }
}
