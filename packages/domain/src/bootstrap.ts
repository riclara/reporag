function escapeRegExp(value: string): string {
  return value.replace(/[.*+?^${}()|[\]\\]/gu, "\\$&");
}

function upsertManagedBlock(
  current: string,
  blockStart: string,
  blockEnd: string,
  managedBlock: string,
  emptyFileTemplate: string,
): string {
  const normalized = current.replace(/\r\n/g, "\n");
  const managedBlockPattern = new RegExp(
    `${escapeRegExp(blockStart)}[\\s\\S]*?${escapeRegExp(blockEnd)}`,
    "u",
  );

  if (managedBlockPattern.test(normalized)) {
    return `${normalized.replace(managedBlockPattern, managedBlock).trimEnd()}\n`;
  }

  const trimmed = normalized.trim();
  if (trimmed.length === 0) {
    return emptyFileTemplate;
  }

  return `${normalized.trimEnd()}\n\n${managedBlock}\n`;
}

